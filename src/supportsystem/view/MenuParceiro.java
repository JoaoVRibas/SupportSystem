    private void listarParceiros() {
        try {
            //ProdutoDAO produtodao = new ProdutoDAO();
            ParceiroNegocioDAO parceirodao = new ParceiroNegocioDAO();
            DefaultTableModel model = (DefaultTableModel) tabelaClientes.getModel();

            model.setNumRows(0);
            List<ParceiroNegocio> lista = parceirodao.listarParceiros();

            for (int i = 0; i < lista.size(); i++) {
                model.addRow(new Object[]{
                    lista.get(i).getId_pn(),
                    lista.get(i).getNome_pn(),
                    lista.get(i).getId_tipo_pn(),
                });
            }

        } catch (Exception ex) {
            System.out.println("Erro ao Listar Parceiros");
            LogController.createLog("Erro ao Listar Parceiros", "W");
        }
    }

